package exam11;

public class Printer {
	
	public int println(int x)
	{
		System.out.println(x);
		return x;
	}
	public boolean println(boolean x)
	{
		System.out.println(x);
		return x;
	}
	public double println(double x)
	{
		System.out.println(x);
		return x;
	}
	public String println(String x)
	{
		System.out.println(x);
		return x;
	}
}
